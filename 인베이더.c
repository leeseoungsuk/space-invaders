void drawmap(char map[20][41], int *PlayerX, int *PlayerY, int *BulletX, int *BulletY, int *cnt, int *player_HP)
{
    mapclear();
    int h, w;
    for (h = 0; h < 20; h++)
    {
        for (w = 0; w < 40; w++)
        {
            char temp = map[h][w];
            if (h == *PlayerX && w == *PlayerY)
            {
                printf("👊");
            }
            else if (h == *BulletX && w == *BulletY)
            {
                printf("🚀");
            }
            else if (temp == '0')
            {
                printf("  ");
            }
            else if (temp == '1')
            {
                printf("🛸");
            }
            else if (temp == '2')
            {
                printf("👽");
            }
            else if (temp == '3')
            {
                printf("🔳");
            }
            else if (temp == '4')
            {
                printf("💥"); // 폭팔 애니메이션 출력
            }
        }
        printf("\n");
    }
    printf("(a: 왼쪽 d: 오른쪽  space: 공격(🚀))");
    printf(" 점수 : %d", *cnt);
    printf(" 체력 : %d", *player_HP);
}

void player_move_1(int *playerX, int *playerY, int moving, int *bullet_x, int *bullet_y, int *bullet_shot, int *cnt)
{
    int newPlayerX = *playerX;
    int newPlayerY = *playerY;
    int newBulletX = *bullet_x;
    int newBulletY = *bullet_y;

    // 사용자의 입력에 따라서 플레이어의 좌표값이 바뀜
    switch (moving)
    {
    case LEFT:
        if (newPlayerY > 0)
        {
            newPlayerY--;
        }
        break;
    case RIGHT:
        if (newPlayerY < 40)
        {
            newPlayerY++;
        }
        break;
    case SPACE:
        if (*bullet_shot == 0) // 장전하기전
        {
            newBulletX = newPlayerX; // 플레이어 좌표 대입
            newBulletY = newPlayerY;
            *bullet_shot = 1; // 장전
        }
        break;
    }

    if (*bullet_shot == 1) // 스페이스 바를 누르면
    {
        if (newBulletX > 0) // 총알의 위치 X 값이 0보다 크면
        {
            newBulletX--; // X값을 하나씩 올린다.
            if (mapFloor1[newBulletX][newBulletY] == '1')
            {
                mapFloor1[newBulletX][newBulletY] = '0';
                *cnt = *cnt + 10;
                *bullet_shot = 0;
            }
            if (mapFloor1[newBulletX][newBulletY] == '3')
            {
                mapFloor1[newBulletX][newBulletY] = '0';
                *bullet_shot = 0;
            }
            if (mapFloor1[newBulletX][newBulletY] == '4')
            {
                mapFloor1[newBulletX][newBulletY] = '0';
                *bullet_shot = 0;
            }
        }
        else
        {
            *bullet_shot = 0;
        }
    }

    if (mapFloor1[newPlayerX][newPlayerY] == '0')
    {
        *playerX = newPlayerX;
        *playerY = newPlayerY;
        *bullet_x = newBulletX;
        *bullet_y = newBulletY;
    }
}

